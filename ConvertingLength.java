package git;

public class ConvertingLength {

    double convertingMtoCm( double base, double cm){
        return base * cm;
    }
    double convertingMtoMm( double base, double mm){
        return base * mm;
    }
    double convertingCmToM( double base, double m){
        return base * m;
    }
    double convertingMmtoM( double base, double m){
        return base * m;
    }

}
